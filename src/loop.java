public class loop {
    public static void main(String[] args) {
        /*
        continue: bỏ qua 1 giá trị nào đó
        break: dừng ngắt khỏi vòng lặp
        */
        for (int i = 1; i<7;i++){
            for (int j =1; j<7; j++){
                System.out.print(i + "" + j + "\t");
            }
            System.out.println("");
        }
        System.out.println("");
        System.out.println("");
        System.out.println("");

        // vẻ chữ n
        for (int i = 1; i<7;i++){
            for (int j =1; j<7; j++){
                System.out.print(i + "" + j + "\t");
            }
            System.out.println("");
        }
    }
}
